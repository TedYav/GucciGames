package voogasalad_GucciGames.gameAuthoring.gui.gaedialog.dialogcomponents;


public class TableElement {
	private String name;
	private String type;
	
	
	public TableElement(String name, String type){
		this.name = name;	
		this.type = type;
	}
	
	public String getName(){
		return name;
	}
	
	public String getType(){
		return type;
	}
	
	

}
